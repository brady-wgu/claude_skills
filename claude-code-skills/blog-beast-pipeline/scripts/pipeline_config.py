def load_config():
    """Load pipeline config from config.json, falling back to defaults.

    Returns a dict with keys: beast_table_id, log_table_id,
    beast_columns, log_columns.
    """
    if os.path.isfile(CONFIG_PATH):
        with open(CONFIG_PATH, "r", encoding="utf-8") as f:
            config = json.load(f)
        # Validate required keys
        required = ["beast_table_id", "log_table_id", "beast_columns", "log_columns"]
        missing = [k for k in required if k not in config]
        if missing:
            print(f"ERROR: config.json is missing keys: {missing}", file=sys.stderr)
            print("Re-run: python scripts/discover_config.py", file=sys.stderr)
            sys.exit(1)
        return config

    # No config.json -- use hardcoded defaults
    return _DEFAULT_CONFIG
